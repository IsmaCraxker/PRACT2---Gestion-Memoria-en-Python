# Creando una lista de frutas
frutas = ["Mango", "Manzana", "Banano", "Guayaba"]
print(frutas)  # Imprimiendo la lista inicial de frutas

# Removiendo 'Manzana' de la lista
frutas.remove("Manzana")
print(frutas)  # Imprimiendo la lista después de remover 'Manzana'

# Añadiendo 'Banano' a la lista de nuevo
frutas.append("Banano")
print(frutas)  # Imprimiendo la lista final de frutas
